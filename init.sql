CREATE TABLE TAPDATA.ORDERS (
	ID INT PRIMARY KEY,
	STREAT VARCHAR2(256),
	CITY VARCHAR2(32),
	STATE VARCHAR2(32),
	COUNTRY VARCHAR2(64),
	ZIP VARCHAR2(32),
	PHONE VARCHAR2(32),
	NAME VARCHAR2(32),
	USER_ID INT
)

CREATE TABLE TAPDATA.ORDER_LINES (
	ID INT PRIMARY KEY,
	ORDER_ID INT,
	PRODUCT_NAME VARCHAR2(128),
	SKU VARCHAR2(16),
	QTY INT,
	PRICE NUMBER,
	CRAP VARCHAR2(1000)
)

CREATE SEQUENCE TAPDATA.TAPSEQ MINVALUE 1 MAXVALUE 9999999999999999999999999999 INCREMENT BY 1 START WITH 1 CACHE 100 ORDER  NOCYCLE;
CREATE SEQUENCE TAPDATA.OLSEQ MINVALUE 1 MAXVALUE 9999999999999999999999999999 INCREMENT BY 1 START WITH 1 CACHE 1000 ORDER  NOCYCLE;